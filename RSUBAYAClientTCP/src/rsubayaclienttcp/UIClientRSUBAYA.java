    public void kirim(String pesan) {
        try {
            if (out == null) {
                out = new DataOutputStream(client.getOutputStream());
            }

            out.writeBytes(pesan);
        } catch (IOException ex) {
            Logger.getLogger(UIClientRSUBAYA.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

