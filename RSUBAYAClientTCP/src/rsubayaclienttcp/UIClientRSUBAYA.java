    public void ambilAntrian() throws IOException {
        try {
            BufferedReader pesanServer = new BufferedReader(new InputStreamReader(client.getInputStream()));
            String pesan;

            while ((pesan = pesanServer.readLine()) != null) {
                String[] data = pesan.split("-");
                if (data.length > 1) {
                    if (data[0].equals(namaPasien)) {
                        txtAreaInfo.append("\nHai nama anda dalam poliknik " + polik + ". Anda mendapatkan nomor antrian " + data[1]);
                    } else if (data[0].equals("info") & data[1].equals(namaPasien) & !"broadcast".equals(data[1])) {
                        txtAreaInfo.append("\n" + data[2]);
                    } else if (data[0].equals("info") & "broadcast".equals(data[1])) {
                        txtAreaInfo.append("\n" + data[2]);
                    }
                }
            }

        } catch (IOException ex) {
        }
    }
